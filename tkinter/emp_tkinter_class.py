class Employee:
	def _init_(self, emp_id = 0, name = "", age = 0, gender = "", place = "", salary = "", prev_company = "", join_date = ""):
		self.emp_id = emp_id
		self.name = name
		self.age = age
		self.gender = gender
		self.place =  place
		self.salary = salary
		self.prev_company = prev_company
		self.join_date = join_date
